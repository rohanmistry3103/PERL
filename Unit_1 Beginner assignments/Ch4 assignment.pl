#Print 'Hello Perl' till the count reaches 10 using While loop and do-While loop


#while loop
$num1= 0;
while ($num1<=9)
{
    print "Hello perl in while loop\n";
    $num1++;
}


#do-While loop
$num2=0;
do
{
    print "Hello Perl in do-while loop\n";
    $num2++;
}
while($num2<=9);

