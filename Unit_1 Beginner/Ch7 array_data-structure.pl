#Number Array
@numArray = (10, 20, 30, 40, 50);
print "Number Array: @numArray\n";

#String Array
@strArray = ('java', 'python', 'perl', 'linux');
print "String Array: @strArray\n";

#Mix Array
@mixarray = (10, 'java', 20, 'python', 30, 'perl');
print "Mixed Array: @mixarray\n";
